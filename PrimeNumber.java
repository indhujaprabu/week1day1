package week1day1;

public class PrimeNumber {

	public static void main(String[] args) {
		int num = 13;
		System.out.println("FIND PRIME NUMBER");
		if (num % 2 == 0)
			System.out.println(num + " is a Prime number");
		else
			System.out.println(num + " is not a Prime number");
	}

}
